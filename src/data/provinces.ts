// Helper functions
export const findProvincesInText = (text: string) => {
  const messageLower = text.toLowerCase();
  const foundProvinces: Array<{
    name: string;
    region: string;
    alias: string;
    position: number;
  }> = [];
  
  for (const [provinceKey, provinceData] of Object.entries(provinces)) {
    for (const alias of provinceData.aliases) {
      if (messageLower.includes(alias.toLowerCase())) {
        foundProvinces.push({
          name: provinceData.name,
          region: provinceData.region,
          alias: alias,
          position: messageLower.indexOf(alias.toLowerCase())
        });
        break;
      }
    }
  }
  
  return foundProvinces;
};

export const getProvinceByAlias = (alias: string) => {
  const lowerAlias = alias.toLowerCase();
  
  for (const [provinceKey, provinceData] of Object.entries(provinces)) {
    if (provinceData.aliases.some(a => a.toLowerCase() === lowerAlias)) {
      return {
        key: provinceKey,
        ...provinceData
      };
    }
  }
  
  return null;
};

export const getProvincesByRegion = (region: string) => {
  return Object.entries(provinces)
    .filter(([key, data]) => data.region === region)
    .map(([key, data]) => ({ key, ...data }));
};

export const getAllRegions = () => {
  const regions = new Set<string>();
  Object.values(provinces).forEach(province => {
    regions.add(province.region);
  });
  return Array.from(regions);
};
